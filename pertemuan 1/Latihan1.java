public class Latihan1 {
    public static void main (String[] args) {
        int i = 1;
    //increment
        System.out.println("1 : " + i);
        System.out.println("++1 : " + ++i);
        System.out.println("1++ : " + i++);
    //decrement
        System.out.println("--1 : " + --i);
        System.out.println("1-- : " + i--);
        System.out.println("1 : " + i);
    }
}